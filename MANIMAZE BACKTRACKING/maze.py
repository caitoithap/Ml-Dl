class Maze(Scene):
    def construct(self):
        scale_factor = 0.7
        square_size = 0.5 * scale_factor
        for i, row in enumerate(maze_matrix):
            for j, val in enumerate(row):
                if val == 0:
                    square = Square(side_length=square_size, color=GREEN, fill_opacity=1).shift((j - len(row) / 2) * square_size * RIGHT + (len(maze_matrix) / 2 - i) * square_size * UP)
                    self.add(square)
                else:
                    square = Square(side_length=square_size, color=WHITE, fill_opacity=1).shift((j - len(row) / 2) * square_size * RIGHT + (len(maze_matrix) / 2 - i) * square_size * UP)
                    self.add(square)

        # Mark entrance and exit
        entrance = Square(side_length=square_size, color=YELLOW, fill_opacity=1).shift(len(row) / 2 * scale_factor * LEFT + (len(maze_matrix) / 2) * square_size * UP)
        exit = Square(side_length=square_size, color=RED, fill_opacity=1).shift((len(row) / 2 - 1) * scale_factor * LEFT + (len(maze_matrix) / 2) * square_size * UP)

        self.add(entrance, exit)
        self.wait(3)

























